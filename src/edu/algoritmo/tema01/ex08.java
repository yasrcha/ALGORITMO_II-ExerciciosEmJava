// a - procedimento [Falso, isso não é um tipo de passagem de parâmetro, e sim um tipo de metodo.]
// b - referência [Verdadeiro. Se o variável original (real) é alterada durante a execução da função, então a passagem é por referência.]
// c - metodo [Falso. Não é um tipo de passagem de parâmetro.]
// d - função [Falso. Não é um tipo de passagem de parâmetro.]
// e - valor [Falso. Passagem de parâmetro por valor não afeta o valor da variável original.]

